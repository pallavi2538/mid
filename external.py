print("To print 10 multiples of a number.")
print("Enter the number: ", end="")
num = int(input())
print("The first 10 multiples of ", num, " are ", end="", sep="")
for i in range(1, 11):  
    i <= 10
    count = num * i
    print(count, ", ", end="", sep="")
print(count, ".", end="", sep="")

    
